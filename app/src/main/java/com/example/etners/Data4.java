package com.example.etners;

public class Data4 {

    String location;
    String building;
    String floor;
    String name;
    String depart;
    String number;
    String workplace;
    String workmanage;
    int id;

    public Data4() {
    }


    public Data4(String location, String building, String floor, String name, String depart, String number, String workplace, String workmanage) {
        this.location = location;
        this.building = building;
        this.floor = floor;
        this.name = name;
        this.depart = depart;
        this.number = number;
        this.workplace = workplace;
        this.workmanage = workmanage;

    }


    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getBuilding() {
        return building;
    }

    public void setBuilding(String building) {
        this.building = building;
    }

    public String getFloor() {
        return floor;
    }

    public void setFloor(String floor) {
        this.floor = floor;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDepart() {
        return depart;
    }

    public void setDepart(String depart) {
        this.depart = depart;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getWorkplace() {
        return workplace;
    }

    public void setWorkplace(String workplace) {
        this.workplace = workplace;
    }

    public String getWorkmanage() {
        return workmanage;
    }

    public void setWorkmanage(String workmanage) {
        this.workmanage = workmanage;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
